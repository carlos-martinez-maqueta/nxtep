        var porcentaje1 = 60;
        var porcentaje2 = 80;
        var porcentaje3 = 30;
        var porcentaje4 = 100;

        animarPorcentaje('porcentaje1', porcentaje1);
        animarPorcentaje('porcentaje2', porcentaje2);
        animarPorcentaje('porcentaje3', porcentaje3);
        animarPorcentajeConSigno('porcentaje4', porcentaje4);

        function animarPorcentaje(elementId, porcentaje) {
          var porcentajeActual = 0;
          var intervalo = setInterval(function () {
            $('#' + elementId).text(porcentajeActual + '%');
            if (porcentajeActual >= porcentaje) {
              clearInterval(intervalo);
            }
            porcentajeActual++;
          }, 20);
        }

        function animarPorcentajeConSigno(elementId, porcentaje) {
          var porcentajeActual = 0;
          var intervalo = setInterval(function () {
            $('#' + elementId).text('+' + porcentajeActual);
            if (porcentajeActual >= porcentaje) {
              clearInterval(intervalo);
            }
            porcentajeActual++;
          }, 20);
        }
      });
